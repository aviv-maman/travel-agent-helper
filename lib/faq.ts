/** All FAQs in display order — the DB when configured, else the in-code set. */
export async function getFaqs(): Promise<Faq[]> {
  if (!usingDatabase()) {
    return DEFAULT_FAQS.map((f, i) => ({ id: i + 1, ...f, sortOrder: i }));
  }
  const { db } = await import("@/db");
  return db.query.faqs.findMany({
    orderBy: (t, { asc }) => [asc(t.sortOrder), asc(t.id)],
  });
}
